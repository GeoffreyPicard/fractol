#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (((char*)s)[i] != '\0')
			((char*)s)[i] = '\0';
		i++;
	}
}
